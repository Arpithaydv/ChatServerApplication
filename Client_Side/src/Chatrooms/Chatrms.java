    public void customSetup(Client_Side cclient, String uusername) {
      
        client= cclient;
        username = uusername;
        client.getChatList(this, new Message("getChatroomList", username, "getlist", "server"));
        jLabel4.setText(uusername);
      
    }
    
    public void chatroom_ame_list(ArrayList<String> clist){
        chatroom_list_ame=clist;
        true_hatroom_name=new ArrayList<String>();
        jTextArea1.setText("");
        for(int i=0;i<chatroom_list_ame.size();i++){
            String txt = jTextArea1.getText();
            jTextArea1.append(chatroom_list_ame.get(i)+"\n");
            int indexOfOpenBracket = chatroom_list_ame.get(i).indexOf('(');
            String trueName = chatroom_list_ame.get(i);
            trueName=trueName.substring(0, indexOfOpenBracket);
            true_hatroom_name.add(trueName);
        }
    }
    
    public void checkNameOfChatroom(String nameOfChatroom){
        //System.out.println("before create message");
        if(true_hatroom_name.contains(nameOfChatroom)){
            JOptionPane.showMessageDialog(null, nameOfChatroom+" Name already exists. Please use another name!!!");
        }else{
            client.send(new Message("Create", username, nameOfChatroom, "server"));
            System.out.println("before create message");
            createroom.dispose();
        }
    }
