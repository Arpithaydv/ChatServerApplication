        // TODO add your handling code here:
        
        String enteredChatRooms = jTextField1.getText();
        enteredChatRooms.trim();
        if(!enteredChatRooms.isEmpty()){
            if(true_hatroom_name.contains(enteredChatRooms)){
                client.joinChatRoom(this, new Message("join", username, enteredChatRooms, "server"));
            }else{
                JOptionPane.showMessageDialog(null, "Please enter the chatroom name available from the list");
            }
            jTextField1.setText("");
        }
